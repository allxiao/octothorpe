// Preferences codegen: the JSON Schema under src/lib/preferences/schema/v<N>/schema.json
// is the single source of truth. This turns the *latest* schema into:
//   generated/types.d.ts   - the `Preferences` TypeScript interface
//   generated/descriptor.ts - an ordered UI descriptor the modal renders from
//   generated/defaults.ts   - the nested default values + LATEST_VERSION
// Run via `pnpm gen:prefs` (also invoked from predev/prebuild). Re-run whenever the
// schema changes; the dev server also regenerates via a Vite watch plugin.

import { readFileSync, writeFileSync, mkdirSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { compile } from "json-schema-to-typescript";

const here = dirname(fileURLToPath(import.meta.url));
const schemaDir = join(here, "..", "src", "lib", "preferences", "schema");
const outDir = join(here, "..", "src", "lib", "preferences", "generated");

/** Highest v<N> directory present under schema/. */
function latestVersion() {
  const versions = readdirSync(schemaDir, { withFileTypes: true })
    .filter((d) => d.isDirectory() && /^v\d+$/.test(d.name))
    .map((d) => Number(d.name.slice(1)));
  if (!versions.length) throw new Error("no schema/v<N> directories found");
  return Math.max(...versions);
}

const VERSION = latestVersion();
const schema = JSON.parse(readFileSync(join(schemaDir, `v${VERSION}`, "schema.json"), "utf8"));

// --- Walk the schema into a flat list of leaf entries -----------------------

/** Collect leaves (properties carrying an `x-ui.control`) with their dot-path keys. */
function collectEntries(node, prefix, out) {
  const props = node.properties ?? {};
  for (const [name, spec] of Object.entries(props)) {
    const key = prefix ? `${prefix}.${name}` : name;
    const ui = spec["x-ui"] ?? {};
    if (spec.type === "object" && spec.properties) {
      collectEntries(spec, key, out);
    } else if (ui.control) {
      out.push({
        key,
        label: spec.title ?? key,
        description: spec.description,
        category: ui.category,
        section: ui.section ?? "General",
        control: ui.control,
        order: ui.order ?? 0,
        default: spec.default,
        options: ui.options,
        min: spec.minimum,
        max: spec.maximum,
        step: ui.step,
        inline: ui.inline,
        unit: ui.unit,
        showWhen: ui.showWhen,
      });
    }
  }
}

const leaves = [];
collectEntries(schema, "", leaves);

// --- Build the ordered UI descriptor ----------------------------------------

const categoryOrder = (schema["x-ui"]?.categories ?? []).map((c) => c.id);
const categoryLabels = Object.fromEntries(
  (schema["x-ui"]?.categories ?? []).map((c) => [c.id, c.label]),
);

const descriptor = [];
for (const catId of categoryOrder) {
  const catLeaves = leaves.filter((l) => l.category === catId);
  if (!catLeaves.length) continue;
  const sections = [];
  for (const leaf of catLeaves) {
    let section = sections.find((s) => s.label === leaf.section);
    if (!section) {
      section = { label: leaf.section, entries: [] };
      sections.push(section);
    }
    const entry = { key: leaf.key, label: leaf.label, control: leaf.control, default: leaf.default };
    if (leaf.description !== undefined) entry.description = leaf.description;
    if (leaf.options !== undefined) entry.options = leaf.options;
    if (leaf.min !== undefined) entry.min = leaf.min;
    if (leaf.max !== undefined) entry.max = leaf.max;
    if (leaf.step !== undefined) entry.step = leaf.step;
    if (leaf.inline !== undefined) entry.inline = leaf.inline;
    if (leaf.unit !== undefined) entry.unit = leaf.unit;
    if (leaf.showWhen !== undefined) entry.showWhen = leaf.showWhen;
    entry.__order = leaf.order;
    section.entries.push(entry);
  }
  for (const s of sections) {
    s.entries.sort((a, b) => a.__order - b.__order);
    for (const e of s.entries) delete e.__order;
  }
  descriptor.push({ id: catId, label: categoryLabels[catId] ?? catId, sections });
}

// --- Build nested defaults --------------------------------------------------

function collectDefaults(node, target) {
  const props = node.properties ?? {};
  for (const [name, spec] of Object.entries(props)) {
    if (spec.type === "object" && spec.properties) {
      const child = {};
      collectDefaults(spec, child);
      if (Object.keys(child).length) target[name] = child;
    } else if (spec.default !== undefined && spec["x-ui"]?.control) {
      target[name] = spec.default;
    }
  }
}

const defaults = {};
collectDefaults(schema, defaults);

// --- Emit -------------------------------------------------------------------

mkdirSync(outDir, { recursive: true });

const banner = "// AUTO-GENERATED by scripts/gen-preferences.mjs — DO NOT EDIT.\n";

const types = await compile(schema, "Preferences", {
  bannerComment: banner.trimEnd(),
  additionalProperties: false,
  style: { singleQuote: false },
});
// json-schema-to-typescript names the root interface after the schema `title`;
// export a stable `Preferences` alias regardless of what that resolves to.
const rootName = (types.match(/export interface (\w+)/) ?? [])[1] ?? "Preferences";
const typesOut =
  types + (rootName === "Preferences" ? "" : `\nexport type Preferences = ${rootName};\n`);
writeFileSync(join(outDir, "types.d.ts"), typesOut);

writeFileSync(
  join(outDir, "descriptor.ts"),
  banner +
    `import type { PrefDescriptor } from "../types";\n\n` +
    `export const DESCRIPTOR: PrefDescriptor = ${JSON.stringify(descriptor, null, 2)};\n`,
);

writeFileSync(
  join(outDir, "defaults.ts"),
  banner +
    `export const LATEST_VERSION = ${VERSION};\n\n` +
    `export const DEFAULTS = ${JSON.stringify(defaults, null, 2)} as const;\n`,
);

console.log(
  `gen-preferences: v${VERSION} → ${descriptor.length} categories, ${leaves.length} entries`,
);
